package com.java.core;

public class Switch_Example {
	public static void main(String[] args) {
		
		int userInput = 8;
		
		switch(userInput) {
		case 1: System.out.println("Jan");
				break;
		case 2: System.out.println("Feb");
				break;
		default: System.out.println("Invalid");
				break;
	}
	}
}
