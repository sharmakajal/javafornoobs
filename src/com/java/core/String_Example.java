package com.java.core;

public class String_Example {
	public static void main(String[] args) {
		System.out.println("Hello World");
		String fname = "Pedro";
		String lname = "Mercado";
		String fl = fname+" "+ lname + Integer.toString(123);
		System.out.println(fl);
		System.out.println(fname + "said, " + "\"he loves chicken\"");
		System.out.println("\tPedro\n said,\n \"he loves chicken\"");

	}
}
